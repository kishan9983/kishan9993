#include <stdio.h>

int main() {
    // Constant declaration
    const float PI = 3.14159;

    // Variable declarations
    int age = 20;
    char grade = 'A';
    float height = 5.9;

    // Displaying the values
    printf("Constant PI: %.2f\n", PI);
    printf("Age (int): %d\n", age);
    printf("Grade (char): %c\n", grade);
    printf("Height (float): %.1f\n", height);

    return 0;
}
