#include <iostream>
using namespace std;

int main() {
    const float PI = 3.14;

    int age = 20;
    float height = 5.6;
    char grade = 'A';
    double salary = 45000.75;

    float circleArea = PI * 5 * 5;
    int nextAge = age + 1;

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;
    cout << "Salary: " << salary << endl;
    cout << "Area of Circle: " << circleArea << endl;
    cout << "Next Year Age: " << nextAge << endl;

    return 0;
}
